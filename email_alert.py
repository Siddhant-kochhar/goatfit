    def _send_gmail_email(self, to_email: str, subject: str, text_body: str, html_body: str) -> bool:
        """Send email using Gmail SMTP"""
        
        # Create message
        message = MIMEMultipart("alternative")
        message["Subject"] = subject
        message["From"] = f"{SENDER_NAME} <{self.gmail_address}>"
        message["To"] = to_email
        
        # Add text and HTML parts
        text_part = MIMEText(text_body, "plain")
        html_part = MIMEText(html_body, "html")
        
        message.attach(text_part)
        message.attach(html_part)
        
        try:
            # Create secure connection and send email
            context = ssl.create_default_context()
            
            with smtplib.SMTP(self.smtp_server, self.smtp_port) as server:
                server.starttls(context=context)  # Enable TLS encryption
                server.login(self.gmail_address, self.gmail_password)
                server.send_message(message)
            
            print(f"âœ… Emergency alert sent successfully to {to_email}")
            return True
            
        except smtplib.SMTPAuthenticationError:
            print("âŒ Gmail authentication failed. Check your email and app password.")
            print("ğŸ’¡ Make sure you're using a Gmail App Password, not your regular password.")
            return False
        except smtplib.SMTPException as e:
            print(f"âŒ SMTP error occurred: {e}")
            return False
        except Exception as e:
            print(f"âŒ Error sending emergency alert: {e}")
            return False
    
